z = [2,6,2,'3',8,]
for v,i in enumerate(z):
    print(v,":",i)
    
    
# enumerate() is a function that returns an enumerate object.
# It takes a sequence as its argument and returns an enumerate object.
# The enumerate object contains a pair of values for each index position in the sequence.
# The first value is the index position and the second value is the value at that index position.
# The enumerate object can be used to loop through the sequence.
